#pragma once

#include <Geoweb3d/core/GW3DInterFace.h>

/*! Primary namespace */
namespace Geoweb3d
{
	////////////////////////////////////////////////////////////////////////////////////////////////////
	/// <summary>	The application inherits from this object and implements the given interface. </summary>
	////////////////////////////////////////////////////////////////////////////////////////////////////
	struct  GW3D_DLL IGW3DBridgedCamera
	{
		////////////////////////////////////////////////////////////////////////////////////////////////////
		/// <summary>	const camera accessor  </summary>
		///
		/// <returns>	the camera </returns>
		////////////////////////////////////////////////////////////////////////////////////////////////////
		virtual IGW3DCamera* get_IGW3DCamera() = 0;

		////////////////////////////////////////////////////////////////////////////////////////////////////
		/// <summary>	non-const camera accessor  </summary>
		///
		/// <returns>	the camera </returns>
		////////////////////////////////////////////////////////////////////////////////////////////////////
		virtual const IGW3DCamera* get_IGW3DCamera() const = 0;

		////////////////////////////////////////////////////////////////////////////////////////////////////
		/// <summary>	query to know whether or not the camera can be modified by clients  </summary>
		///
		/// <param name="requestor">	A friendly name should be passed in, representing the requestor </param>
		///
		/// <returns>	true if the camera can be modified, otherwise false </returns>
		////////////////////////////////////////////////////////////////////////////////////////////////////
		virtual bool get_CanCameraBeModified( const char* requestor ) const = 0;

		////////////////////////////////////////////////////////////////////////////////////////////////////
		/// <summary>	query for the reason why the camera currently cannot be modified.  </summary>
		///
		/// <returns>	a string indicating the reason why get_CanCameraBeModified returned false  </returns>
		////////////////////////////////////////////////////////////////////////////////////////////////////
		virtual IGW3DStringPtr get_Reason() const = 0;
	};
}
